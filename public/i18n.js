function t(key) {
  const lang = window.APP_LANG || 'uz_latn';
  return (i18n[lang] && i18n[lang][key]) || i18n['ru'][key] || key;
}

function getQuestionText(q) {
  const lang = window.APP_LANG || 'uz_latn';
  if (lang === 'uz_cyrl' && q.savol_kril) return q.savol_kril;
  if (lang === 'ru' && q.savol_rus) return q.savol_rus;
  if (lang === 'ru' && !q.savol_rus && q.savol_kril) return q.savol_kril;
  return q.savol || q.savol_kril || q.savol_rus || '';
}

function getAnswerText(q, ansKey) {
  // ansKey = 'javob_1', 'javob_2', etc.
  const lang = window.APP_LANG || 'uz_latn';
  if (lang === 'uz_cyrl') {
    const krilKey = ansKey + '_kril';
    if (q[krilKey]) return q[krilKey];
  }
  if (lang === 'ru') {
    const rusKey = ansKey + '_rus';
    if (q[rusKey]) return q[rusKey];
    const krilKey = ansKey + '_kril';
    if (q[krilKey]) return q[krilKey];
  }
  return q[ansKey] || '';
}
